    public void theMachineAwakens(){
        try{
            BufferedImage image = ImageIO.read(new File(System.getProperty("user.dir")+"/src/terminator.jpeg"));
            JLabel picLabel = new JLabel(new ImageIcon(image));
            JPanel jPanel = new JPanel();
            jPanel.add(picLabel);
            JFrame f = new JFrame();
            f.setSize(new Dimension(image.getWidth(), image.getHeight()));
            f.add(jPanel);
            f.setVisible(true);
            TimeUnit.MILLISECONDS.sleep(2000);
            f.dispose();
        } catch (IOException e){
            System.out.println(e.getMessage());
        } catch (InterruptedException e){
            System.out.println(e.getMessage());
        }
    }

